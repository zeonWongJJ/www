public class CommonEventEntity<T> implements Serializable {

    public int what;
    public T obj;

    public CommonEventEntity() {
        super();
    }

    public CommonEventEntity(int what) {
        super();
        this.what = what;
    }

    public CommonEventEntity(int what, T t) {
        super();
        this.what = what;
        this.obj = t;
    }
}
