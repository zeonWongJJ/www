namespace utils\interfaces;

interface BaseRepositoryInterface
{
    /**
     * 必须实现数据列表获取方法
     * @return mixed
     */
    public function getList();

    /**
     * 必须实现数据单条获取方法
     * @return mixed
     */
    public function getOne();

    /**
     * 必须实现数据更新方法
     * @return mixed
     */
    public function update();

    /**
     * 必须实现数据新增方法
     * @return mixed
     */
    public function insert();

    /**
     * 必须实现数据删除方法
     * @return mixed
     */
    public function delete();
}